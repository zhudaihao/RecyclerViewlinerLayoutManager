package cn.zdh.recyclerview_linerlayoutmanager;

public class VideoBean {
    private int video;
    private int image;

    public VideoBean(int video, int image) {
        this.video = video;
        this.image = image;
    }

    public int getVideo() {

        return video;
    }

    public void setVideo(int video) {
        this.video = video;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
